    private void tblJenisMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblJenisMouseClicked
        btnSimpan.setEnabled(false);
        int i = tblJenis.getSelectedRow();
        if (i == -1){
            return;
        }
        
        String ID_Jenis = (String)model.getValueAt(i, 0);
        txtJenis1.setText(ID_Jenis);
        String Jenis_Barang = (String)model.getValueAt(i, 1);
        txtNama.setText(Jenis_Barang);
    }//GEN-LAST:event_tblJenisMouseClicked

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String ID_Jenis = txtJenis1.getText();
        String Jenis_Barang = txtNama.getText();
        
        
        try  {
            if(txtNama.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Tidak boleh kosong");
            }else{
            Connection c = dbJenisBarang.getKoneksi();
            String sql = "INSERT INTO jenis VALUES (?, ?)";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, ID_Jenis);
            p.setString(2, Jenis_Barang);
            p.executeUpdate();
            p.close();
            JOptionPane.showMessageDialog(null, "Data Tersimpan");
            }
        }catch(SQLException e){
            System.out.println("Terjadi Kesalahan");
        }finally{
            loadData();
            autonumber();
            clear();
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        int i = tblJenis.getSelectedRow();
        if(i == -1){
            return;
        }
        String ID_Jenis = (String)model.getValueAt(i, 0);
        String Jenis_Barang = txtNama.getText();
        
        try{
            Connection c = dbJenisBarang.getKoneksi();
            String sql = "UPDATE jenis SET Jenis_Barang = ? WHERE ID_Jenis = ?";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, Jenis_Barang);
            p.setString(2, ID_Jenis);            
            p.executeUpdate();
            p.close();
            JOptionPane.showMessageDialog(null, "Data Terubah");
            btnSimpan.setEnabled(true);
            clear();
        }catch(SQLException e){
            System.out.println("Update Error");
        }finally{
            loadData();
            autonumber();
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int i = tblJenis.getSelectedRow();
        if(i == -1){
            return;
        }
        
        String ID_Jenis = (String)model.getValueAt(i, 0);
        
        int question = JOptionPane.showConfirmDialog(null, "Yakin Data Akan Dihapus?","Konfirmasi", JOptionPane.OK_CANCEL_OPTION,
                JOptionPane.QUESTION_MESSAGE);
        if(question == JOptionPane.OK_OPTION){
            try{
                Connection c = dbJenisBarang.getKoneksi();
                String sql = "DELETE FROM jenis WHERE ID_Jenis = ?";
                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, ID_Jenis);
                p.executeUpdate();
                p.close();
                JOptionPane.showMessageDialog(null, "Data Terhapus");
            }catch(SQLException e){
                System.out.println("Terjadi Kesalahab");
            }finally{
                loadData();
                autonumber();
                clear();
            }
        }
        if(question == JOptionPane.CANCEL_OPTION){
        }
    }//GEN-LAST:event_btnHapusActionPerformed

