    private void autonumber(){
        try{
            Connection c = dbJenisBarang.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM jenis ORDER BY ID_Jenis DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()){
                String ID_Jenis = r.getString("ID_Jenis").substring(2);
                String JN = "" + (Integer.parseInt(ID_Jenis) + 1);
                String Nol = "";
                
                if(JN.length()==1){
                    Nol = "00";
                }else if(JN.length()==2){
                    Nol = "0";
                }else if(JN.length()==3){
                    Nol = "";
                }
                
                txtJenis1.setText("JN" + Nol + JN);
                
            }else{
                txtJenis1.setText("JN001");
            }
            r.close();
            s.close();
        }catch(Exception e){
            System.out.println("autonumber error");
        }
    }
    
